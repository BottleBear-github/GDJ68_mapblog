package com.gdj68.mapblog.member;

public class IgnoreDTO {
	
	private long ignoreNum;
	private String ignoring;
	private String ignored;
	
	public long getIgnoreNum() {
		return ignoreNum;
	}
	public void setIgnoreNum(long ignoreNum) {
		this.ignoreNum = ignoreNum;
	}
	public String getIgnoring() {
		return ignoring;
	}
	public void setIgnoring(String ignoring) {
		this.ignoring = ignoring;
	}
	public String getIgnored() {
		return ignored;
	}
	public void setIgnored(String ignored) {
		this.ignored = ignored;
	}
}
